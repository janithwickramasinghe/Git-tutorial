# calculator.py
def add(a, b):
    """Returns the sum of two numbers."""
    return a + b

def subtract(a, b):
    """Returns the difference of two numbers."""
    return a - b

# Example usage
if __name__ == "__main__":
    result = add(5, 3)
    print("The sum is:", result)

    result_sub = subtract(5, 3)
    print("The sum is:", result_sub)
